// Operadores de comparacion

/* 

==(igualdad): Evalua si los valores son iguales con conversion de tipo.

===(igualdad estricta): devuelve true solo si los valores son iguales en valor y en tipo de dato.

!=(desigualdad): devuelve true si los valores son diferentes.

!==(desigualdad estricta): devuelve true si son diferentes en valor y en tipo de dato.

<(menor que): devuelve true si el valor seleccionado es menor al valor con el cual se esta comparando

>(mayor que): devuelve true si el valor seleccionado es mayor al valor con el cual se esta comparando

<=(menor o igual que): devuelve true si el valor seleccionado es menor o igual al valor con el cual se esta comparando

>=(mayor que): devuelve true si el valor seleccionado es mayor o igual al valor con el cual se esta comparando

*/


